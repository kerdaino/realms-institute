export async function recordLmsAudit(supabase: SupabaseClient, input: {
  action: LmsAuditAction;
  entityType: "student" | "student_enrollment" | "cohort" | "course" | "facilitator" | "class_session" | "class_summary" | "session_resource" | "class_recording" | "session_attendance" | "course_enrollment" | "recording_learning_assignment" | "session_learning_completion" | "assignment" | "assignment_submission" | "quiz" | "quiz_attempt" | "absence_request" | "makeup_requirement" | "student_engagement_alert" | "student_warning_notice" | "mentor_assignment" | "mentor_followup" | "student_recovery_plan" | "recovery_plan_action" | "student_status_review_case" | "student_support_referral" | "programme_scoring_policy" | "assessment_weighting" | "engagement_component_evaluation" | "capstone_defence" | "student_graduation_requirement" | "student_programme_result" | "academic_result_batch" | "graduation_confirmation" | "alumni" | "alumni_programme_record" | "alumni_course_archive" | "alumni_summary_archive_item" | "alumni_recording_access_grant" | "certificate_template" | "institutional_award" | "alumni_announcement" | "alumni_outcome_update";
  entityId: string;
  metadata?: Record<string, unknown>;
  actorUserId?: string | null;
}) {
  const { error } = await supabase.from("audit_logs").insert({
    action: input.action,
    entity_type: input.entityType,
    entity_id: input.entityId,
    metadata: input.metadata ?? {},
    actor_user_id: input.actorUserId ?? null,
  });
  if (error) console.error("LMS audit insert failed after the administrative change was saved", { action: input.action, code: error.code });
}
